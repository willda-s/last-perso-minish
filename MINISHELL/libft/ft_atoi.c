#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	res;
	int	nb;

	i = 0;
	nb = 0;
	res = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			res *= (-1);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
	}
	return (nb * res);
}
